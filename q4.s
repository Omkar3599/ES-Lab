	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=A
	LDR R0,[R0]
	LDR R1,=B
	LDR R1,[R1]
	MOV R2,#1
LOOP
	MUL R3,R0,R2
	MOV R4,R3
REMLOOP
	CMP R4,R1
	BLT REMDONE
	SUB R4,R4,R1
	B REMLOOP
REMDONE
	CMP R4,#0
	BEQ FOUNDLCM
	ADD R2,R2,#1
	B LOOP
	
FOUNDLCM
	LDR R7,=LCM
	STR R3,[R7]
STOP B STOP
A DCD 12
B DCD 18
	AREA mydata,DATA,READWRITE
LCM DCD 0
	END
	