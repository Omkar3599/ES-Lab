	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,= SRC
	LDR R1,[R0]
	LDR R4,=DSTN
	MOV R5,#4
LOOP
	AND R2,R1,#0XF
	CMP R2,#9
	ADDHI R2,R2,#0X37
	ADDLS R2,R2,#0X30
	STRB R2,[R4],#1
	LSR R1,#4
	SUB R5,#1
	BNE LOOP
STOP B STOP
SRC DCD 0XA
	AREA mydata,DATA,READWRITE
DSTN DCD 0,0,0,0
	END
	