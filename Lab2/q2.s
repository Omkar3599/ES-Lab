	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10002000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	MOV R3, #5
	LDR R0,=SRC
	LDR R1,=DSTN
BACK	LDR R2, [R0], #4
	STR R2, [R1], #4
	SUB R3, #1
	CMP R3, #0
	BNE BACK	
STOP B STOP
SRC DCD 0x1100,0x1345,0x1543,0xA123,0xF3
	AREA mydata,DATA,READWRITE
DSTN DCD 0,0,0,0,0
	END