	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R3,= SRC
	MOV	R4,#10
UP
	LDR R0,[R3],#4
	ADDS R1,R0
	ADC R2,#0
	SUBS R4,#1
	BNE UP
	
	LDR R3,=RESULT
	STR R1,[R3],#4
	STR R2,[R3]
STOP B STOP
SRC DCD 0X12,0X13,0X1,0X2,0X3,0X4,0X5,0X6,0X7,0X21
	AREA mydata,DATA,READWRITE
RESULT DCD 0,0
	END